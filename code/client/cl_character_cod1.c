static const char *CL_TP_GscReadFile( void *ctx, const char *filename, int *status )
{
    char sourcePath[MAX_QPATH];
    char *fileText;
    long fileLen;
    char *copy;

    (void)ctx;

    CL_TP_GscBuildSourcePath( filename, sourcePath, sizeof( sourcePath ) );

    fileText = NULL;
    fileLen = FS_ReadFile( sourcePath, (void **)&fileText );
    if ( fileLen <= 0 || !fileText ) {
        *status = GSC_NOT_FOUND;
        return NULL;
    }

    copy = (char *)malloc( (size_t)fileLen + 1 );
    if ( !copy ) {
        FS_FreeFile( fileText );
        *status = GSC_OUT_OF_MEMORY;
        return NULL;
    }

    if ( fileLen > 0 ) {
        memcpy( copy, fileText, (size_t)fileLen );
    }
    copy[fileLen] = '\0';

    FS_FreeFile( fileText );
    CL_TP_GscTrackSource( copy );
    *status = GSC_OK;
    return copy;
}

